class Veiculo {
    String placa;
    String modelo;
    int anoFabricacao;
    String cor;

    Veiculo(String placa, String modelo, int anoFabricacao, String cor) {
        this.placa = placa;
        this.modelo = modelo;
        this.anoFabricacao = anoFabricacao;
        this.cor = cor;
    }
}
